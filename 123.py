#爷的第一个程序
import turtle
t=turtle.Pen()
t.width(7)

t.penup()
t.goto(-200,200)
t.pendown()
t.right(90)
t.forward(100)
t.goto(-200,200)
t.left(90)
t.forward(40)
t.penup()
t.goto(-200,150)
t.pendown()
t.forward(40)
t.penup()

t.goto(-150,200)
t.right(90)
t.pendown()
t.forward(100)
t.left(90)
t.forward(40)
t.left(90)
t.forward(100)
t.penup()

t.goto(-40,200)
t.pendown()
t.left(90)
t.forward(40)
t.left(90)
t.forward(100)
t.left(90)
t.forward(40)
t.penup()

t.goto(-30,200)
t.right(90)
t.pendown()
t.forward(100)
t.goto(-30,150)
t.goto(10,100)
t.goto(-30,150)
t.goto(10,200)
t.penup()

t.goto(-10,0)
t.pendown()
t.goto(10,-50)
t.penup()
t.goto(30,0)
t.pendown()
t.goto(10,-50)
t.goto(10,-100)
t.penup()

t.goto(40,0)
t.pendown()
t.forward(100)
t.left(90)
t.forward(40)
t.left(90)
t.forward(100)
t.left(90)
t.forward(40)
t.penup()

t.goto(90,0)
t.pendown()
t.left(90)
t.forward(100)
t.left(90)
t.forward(40)
t.left(90)
t.forward(100)
t.penup()
